    void calc(double num1, double num2, char ope){
        double result = 0.0;
        switch (ope) {
            case '+' -> result =  num1 + num2;
            case '-' -> result =  num1 - num2;
            case '*' -> result =  num1 * num2;
            case '/' -> result =  num1 / num2;
            case '%' -> result =  num1 % num2;
            default -> System.out.println("other");
        }
        lblResult.setText(String.valueOf(result));
    }
    
